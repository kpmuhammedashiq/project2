package model;

public class GradeCard {
	private int language;

	private int Mathematics;

	private int physicsMark;
	private int chemistryMark;

	private int biologyMark;

	private int historyMark;

	private int geographyMark;

	private int totalMark;

	private int averageMark;

	public int getLanguage() {
		return language;
	}

	public void setLanguage(int language) {
		this.language = language;
	}

	public int getMathematics() {
		return Mathematics;
	}

	public void setMathematics(int mathematics) {
		Mathematics = mathematics;
	}

	public int getPhysicsMark() {
		return physicsMark;
	}

	public void setPhysicsMark(int physicsMark) {
		this.physicsMark = physicsMark;
	}

	public int getChemistryMark() {
		return chemistryMark;
	}

	public void setChemistryMark(int chemistryMark) {
		this.chemistryMark = chemistryMark;
	}

	public int getBiologyMark() {
		return biologyMark;
	}

	public void setBiologyMark(int biologyMark) {
		this.biologyMark = biologyMark;
	}

	public int getHistoryMark() {
		return historyMark;
	}

	public void setHistoryMark(int historyMark) {
		this.historyMark = historyMark;
	}

	public int getGeographyMark() {
		return geographyMark;
	}

	public void setGeographyMark(int geographyMark) {
		this.geographyMark = geographyMark;
	}

	public int getTotalMark() {
		return totalMark;
	}

	public void setTotalMark(int totalMark) {
		this.totalMark = totalMark;
	}

	public int getAverageMark() {
		return averageMark;
	}

	public void setAverageMark(int averageMark) {
		this.averageMark = averageMark;
	}

}
