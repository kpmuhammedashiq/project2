package model;

public class Student {
	private String name;
	private String regNo;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRegNo() {
		return regNo;
	}

	public void setRegNo(String string) {
		this.regNo = string;
	}

}